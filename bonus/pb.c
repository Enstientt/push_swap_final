#include "checker.h"

void	push_b(t_nums **a, t_nums **b)
{
	t_nums	*bot_a;

	if (*a == NULL)
		return ;
	bot_a = (*a)->next;
	(*a)->next = *b;
	*b = *a;
	*a = bot_a;
}
